class Solution(object):
    def romanToInt(self, s):
        d = {
         "I":1,
         "IV":4,
         "V":5,
         "IX":9,
         "X":10,
         "L":50,
         "C":100,
         "D":500,
         "M":1000
        }
        total = 0
        for i in range(len(s)):
            if i < len(s) - 1 and d[s[i]] < d[s[i+1]]:
                total -= d[s[i]]
            else:
                total += d[s[i]]
        return total                
        
        